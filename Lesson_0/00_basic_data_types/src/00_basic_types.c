#include <stdio.h>

int main(void) {
    int counter = 100;
    float temperature = 36.5f;
    double preciseValue = 3.141592653589793;
    char letter = 'A';

    printf("counter: %d\n", counter);
    printf("temperature: %.2f\n", temperature);
    printf("preciseValue: %lf\n", preciseValue);
    printf("letter: %c\n", letter);

    return 0;
}
